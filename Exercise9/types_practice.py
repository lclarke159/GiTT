# Setting name variables
first_name = 'Lewis'
last_name = "Clarke"

# print full name with a space
full_name_with_space = first_name + ' ' + last_name
print(full_name_with_space)

# store name as list
my_name_list = [first_name, last_name]
print(my_name_list)
print(type(my_name_list))

# store name as dictionary
my_name_dictionary = {first_name: 'first', last_name: 'last'}

print(my_name_dictionary)
print(type(my_name_dictionary))
